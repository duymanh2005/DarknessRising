mc.resource.getPreLoadSpineURL = function (index, allSpineURL, enableHitEffect) {
    allSpineURL = allSpineURL || [];
    var asset = mc.dictionary.getCreatureAssetByIndex(index);
    if (asset) {
        var spineStr = asset.getSpineString();
        allSpineURL.push(spineStr + ".json");
        !cc.sys.isNative && allSpineURL.push(spineStr + ".atlas");
        spineStr = spineStr.split(/[0-9]/)[0];
        allSpineURL.push(spineStr + ".png");

        if (enableHitEffect) {
            var prefixFolder = "res/spine/char/battle_effect/";
            asset = asset.getData();
            var arrSpine = asset["attackHitEffect"];
            if (arrSpine) {
                for (var i = 0; i < arrSpine.length; i++) {
                    allSpineURL.push(prefixFolder + arrSpine[i] + ".json");
                    allSpineURL.push(prefixFolder + arrSpine[i] + ".png");
                    !cc.sys.isNative && allSpineURL.push(prefixFolder + arrSpine[i] + ".atlas");
                }
            }
            arrSpine = asset["skillAutoHitEffect"];
            if (arrSpine) {
                for (var i = 0; i < arrSpine.length; i++) {
                    allSpineURL.push(prefixFolder + arrSpine[i] + ".json");
                    allSpineURL.push(prefixFolder + arrSpine[i] + ".png");
                    !cc.sys.isNative && allSpineURL.push(prefixFolder + arrSpine[i] + ".atlas");
                }
            }
            arrSpine = asset["skillActiveHitEffect"];
            if (arrSpine) {
                for (var i = 0; i < arrSpine.length; i++) {
                    allSpineURL.push(prefixFolder + arrSpine[i] + ".json");
                    allSpineURL.push(prefixFolder + arrSpine[i] + ".png");
                    !cc.sys.isNative && allSpineURL.push(prefixFolder + arrSpine[i] + ".atlas");
                }
            }
        }
    }
    return allSpineURL;
};

mc.resource.getPreLoadSoundURL = function (index, allSoundURL) {
    allSoundURL = allSoundURL || [];
    var asset = mc.dictionary.getCreatureAssetByIndex(index);
    if (asset) {
        asset = asset.getData();
        var prefixFolder = "res/sound/effect/";
        var arrSound = asset["attackSound"];
        if (arrSound) {
            for (var i = 0; i < arrSound.length; i++) {
                allSoundURL.push(prefixFolder + arrSound[i] + ".mp3");
            }
        }
        var arrSound = asset["skillActiveSound"];
        if (arrSound) {
            for (var i = 0; i < arrSound.length; i++) {
                allSoundURL.push(prefixFolder + arrSound[i] + ".mp3");
            }
        }
        arrSound = asset["skillAutoSound"];
        if (arrSound) {
            for (var i = 0; i < arrSound.length; i++) {
                allSoundURL.push(prefixFolder + arrSound[i] + ".mp3");
            }
        }
        if (asset["hurtSound"]) {
            allSoundURL.push(prefixFolder + asset["hurtSound"] + ".mp3");
        }
    }
    return allSoundURL;
};

mc.resource.getBattleTeamPreLoadURL = function (teamId) {
    var arrRes = [];
    var arrHeroId = mc.GameData.teamFormationManager.getTeamFormationByIndex(teamId);
    for (var i = 0; i < arrHeroId.length; i++) {
        if (arrHeroId[i] > 0) {
            var heroInfo = mc.GameData.heroStock.getHeroById(arrHeroId[i]);
            if (heroInfo) {
                arrRes = mc.resource.getPreLoadSpineURL(mc.HeroStock.getHeroIndex(heroInfo), arrRes);
            }
        }
    }
    //trim
    var map = bb.utility.arrayToMap(arrRes, function (url) {
        return url;
    });
    return bb.utility.mapToArray(map);
};

